# Page config
# ---------------------------------------------------------------------------

st.set_page_config(page_title="ML Bot", page_icon="🤖", layout="wide")

st.markdown(
    """
    <style>
        .block-container { padding-top: 1.2rem; max-width: 1000px; }
        .bot-title  { text-align: center; font-size: 34px; font-weight: 700; margin-bottom: 20px; }
        .bot-footer { text-align: center; color: gray; font-size: 12px; margin-top: 20px; }
    </style>
    """,
    unsafe_allow_html=True,
)

st.markdown("<div class='bot-title'>🤖 ML Bot</div>", unsafe_allow_html=True)


# ---------------------------------------------------------------------------
# Session state
# ---------------------------------------------------------------------------

if "messages"  not in st.session_state: st.session_state.messages  = []
if "chat_id"   not in st.session_state: st.session_state.chat_id   = 1
if "all_chats" not in st.session_state: st.session_state.all_chats = {}


# ---------------------------------------------------------------------------
# Sidebar
# ---------------------------------------------------------------------------

with st.sidebar:
    st.title("🤖 ML Bot")
    st.caption(f"Chat #{st.session_state.chat_id}")
    st.divider()

    st.markdown("### Pipeline")
    st.write("✅ Embedding : MiniLM")
    st.write("✅ Chunking  : Hybrid")
    st.write("✅ Retrieval : MMR + Reranker")
    st.write("✅ LLM       : TinyLlama 1.1B (4-bit)")
    st.divider()

    if st.button("➕ New Chat", use_container_width=True, type="primary"):
        if st.session_state.messages:
            st.session_state.all_chats[st.session_state.chat_id] = (
                st.session_state.messages.copy()
            )
        st.session_state.chat_id  += 1
        st.session_state.messages  = []
        st.rerun()

    if st.button("🗑  Clear Chat", use_container_width=True):
        st.session_state.messages = []
        st.rerun()

    st.divider()
    st.markdown("### History")

    if not st.session_state.all_chats:
        st.caption("No previous chats yet.")
    else:
        for num in sorted(st.session_state.all_chats, reverse=True):
            if st.button(f"💬 Chat #{num}", key=f"hist_{num}", use_container_width=True):
                st.session_state.chat_id  = num
                st.session_state.messages = st.session_state.all_chats[num]
                st.rerun()

    st.divider()
    st.caption("📚 Machine Learning Book Assistant")


# ---------------------------------------------------------------------------
# Chat history display
# ---------------------------------------------------------------------------

for msg in st.session_state.messages:
    with st.chat_message(msg["role"]):
        st.markdown(msg["content"])


# ---------------------------------------------------------------------------
# Input + response
# ---------------------------------------------------------------------------

query = st.chat_input("Ask anything about Machine Learning …")

if query:
    # Store and display user message
    st.session_state.messages.append({"role": "user", "content": query})
    with st.chat_message("user"):
        st.markdown(query)

    # Build conversation history string (last 10 turns)
    recent = st.session_state.messages[-10:]
    history = "".join(
        f"{'User' if m['role'] == 'user' else 'Assistant'}: {m['content']}\n"
        for m in recent
    )

    enhanced_query = (
        f"Previous Conversation:\n{history}\n"
        f"Current Question:\n{query}\n"
    )

    # Generate and display assistant response
    with st.chat_message("assistant"):
        with st.spinner("Thinking …"):
            result = ask_query(
                user_input_with_history = enhanced_query,
                embedding_model_name    = DEFAULT_EMBED_MODEL,
                chunk_type              = DEFAULT_CHUNK_TYPE,
                k_retrieval             = DEFAULT_K,
                lambda_mult             = DEFAULT_LAMBDA,
                fetch_k                 = DEFAULT_FETCH_K,
            )

        answer = result["answer"]
        st.markdown(answer)

        # Sources
        if result.get("sources"):
            with st.expander("📚 Sources"):
                for src in result["sources"]:
                    label = (
                        f"{src['citation']}  "
                        f"Page {src['page_number']} · Chunk {src['chunk_id']} · "
                        f"Score {src['rerank_score']:.4f}"
                    )
                    with st.expander(label):
                        st.markdown(src["text"])

        # Metrics
        c1, c2 = st.columns(2)
        c1.metric("Avg Rerank Score",  result.get("avg_rerank_score", 0))
        c2.metric("Response Time",     f"{result.get('response_time_seconds', 0)} s")

    # Persist assistant message
    st.session_state.messages.append({"role": "assistant", "content": answer})
    st.session_state.all_chats[st.session_state.chat_id] = st.session_state.messages.copy()


